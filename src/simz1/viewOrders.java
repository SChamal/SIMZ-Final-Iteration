    public viewOrders() {
        initComponents();
        
        ResultSet rst = dbOps.getOrderIDs();
        try {
            while(rst.next()){
                dlm.addElement("Order " + rst.getInt(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(viewOrders.class.getName()).log(Level.SEVERE, null, ex);
        }
        listOrderIDs.setModel(dlm);
        this.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
        MouseListener mouseListener = new MouseAdapter() {
        public void mouseClicked(MouseEvent e) {
        if (e.getClickCount() == 2) { //double click event
            int index = listOrderIDs.locationToIndex(e.getPoint());
            String orderID = dlm.getElementAt(index).toString();
            int number =Integer.parseInt(orderID.substring(6));
            ResultSet rs = dbOps.combineProductDetailsAndOrder_2(number);
            viewOrderTableModel model= new viewOrderTableModel();
            tblViewOrders.setModel((TableModel) model);
            try {
                while(rs.next()){
                    model.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getString(3)});
                }
            } catch (SQLException ex) {
                Logger.getLogger(viewOrders.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        }
        };
        listOrderIDs.addMouseListener(mouseListener);
    }

